    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {

        ResultSet ac = Database.getData("SELECT * FROM users WHERE email = '"+UidLbl.getText()+"'");
        try {

            if(ac.next()){

                BCrypt.Result verify = BCrypt.verifyer().verify(PasswordLbl.getText().toCharArray(), ac.getString("password"));

                if(verify.verified){

                    int userid = ac.getInt("user_id");
                    String username = ac.getString("name");
                    String useremail = ac.getString("email");
                    String userlevel = ac.getString("role");
                    //                String usertel = ac.getString("tel");

                    cache.LogUserId = userid;
                    cache.LogUserName = username;
                    cache.LogUserEmail = useremail;
                    cache.LogUserLevel = userlevel;
                    //                cache.LogUserTel = usertel;

                    new Home().setVisible(true);
                    this.dispose();

                }else{

                    JOptionPane.showMessageDialog(rootPane, "Wrong Credentials.");

                }

            }else{

                JOptionPane.showMessageDialog(rootPane, "Wrong User name or Password.");

            }
        }catch (SQLException ex){
            ex.printStackTrace();
        }
    }

