    private void SaveNameBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveNameBtnActionPerformed

        if(NewNameField.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill the requered(*)  area.");
        }else{

            String UserId = String.valueOf(cache.LogUserId);
            String UserName = NewNameField.getText();

            try {
                JSONObject jo = ServerController.getJsonObject(WebServiceController.UPDATE_USER_NAME, ServerController.updateUserNameParams(UserId,UserName));
                if(jo != null){

                    if(jo.getBoolean("result")){

                        JOptionPane.showMessageDialog(null, "User name update successfully.");
                        String NUserName = jo.getString("NewUserName");

                        UserNameLbl.setText(NUserName);
                        NewNameField.setText(NUserName);

                    }else{
                        System.out.println("error");
                        JOptionPane.showMessageDialog(null, "Error updating user name.");
                    }

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_SaveNameBtnActionPerformed

