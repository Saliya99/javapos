    private void UpdateShopDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateShopDetailsActionPerformed

        if(ShopNameField.getText().isEmpty() || ShopEmailField.getText().isEmpty() || ShopContactNumberField.getText().isEmpty() || ShopAddressField.getText().isEmpty() || ShopRodatNameField.getText().isEmpty() || ShopCityField.getText().isEmpty() || ShopCountryField.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill the requered(*)  area.");
        }else{

            String ShopName = ShopNameField.getText();
            String ShopEmail = ShopEmailField.getText();
            String ShopTel = ShopContactNumberField.getText();
            String ShopAddress = ShopAddressField.getText();
            String ShopRoadName = ShopRodatNameField.getText();
            String ShopCityName = ShopCityField.getText();
            String ShopCountryName = ShopCountryField.getText();

            try {
                JSONObject jo = ServerController.getJsonObject(WebServiceController.UPDATE_SHOP_DETAILS, ServerController.sendShopDetailsParams(ShopName,ShopEmail,ShopTel,ShopAddress,ShopRoadName,ShopCityName,ShopCountryName));
                if(jo != null){

                    if(jo.getBoolean("result")){

                        JOptionPane.showMessageDialog(null, "Shop details updated successfully.");
                        loadData();

                    }else{
                        System.out.println("error");
                        JOptionPane.showMessageDialog(null, "Error updating shop details, Please try again.");
                    }

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_UpdateShopDetailsActionPerformed

