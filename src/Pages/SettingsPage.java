    private void UpdateVATDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateVATDetailsBtnActionPerformed

        if(InvoiceVatField.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill the requered(*)  area.");
        }else{

            String NewVatRate = InvoiceVatField.getText();

            try {
                JSONObject jo = ServerController.getJsonObject(WebServiceController.UPDATE_VAT_DETAILS, ServerController.sendVatDetailsParams(NewVatRate));
                if(jo != null){

                    if(jo.getBoolean("result")){

                        JOptionPane.showMessageDialog(null, "VAT rate updated successfully.");
                        loadData();

                    }else{
                        System.out.println("error");
                        JOptionPane.showMessageDialog(null, "Error updating VAT rate, Please try again.");
                    }

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_UpdateVATDetailsBtnActionPerformed

