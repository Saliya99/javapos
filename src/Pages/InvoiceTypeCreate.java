    private void LoadAllClientsData(){
        //dtm.setRowCount(0);

        SelectClientIdField.addItem(new Customers(0, "PLEASE SELECT A CLIENT",""));

        allclientsThread = new Thread(new Runnable() {
        @Override
        public void run() {

            try {
                 JSONObject jo = ServerController.getJsonObject(WebServiceController.DOWNLOAD_CLIENT_LIST, ServerController.getClientListParams("1"));
            if(jo != null){

                if(jo.getBoolean("result")){


                    JSONArray array = jo.getJSONArray("data");

                    for(int i = 0; i<array.length() ;i++){

                            JSONObject pd = array.getJSONObject(i);

                            int ClientId = pd.getInt("ClientId");
                            String ClientFirstName = pd.getString("ClientFirstName");
                            String ClientLastName = pd.getString("ClientLastName");
                            String ClientContactNumber = pd.getString("ClientContactNumber");

                            //String ClientDisplayName = ClientFirstName+" "+ClientLastName;


                            Customers client = new Customers();
                            client.setClientId(ClientId);
                            client.setClientFirstName(ClientFirstName);
                            client.setClientLastName(ClientLastName);
                            client.setClientContactNumber();

                            SelectClientIdField.addItem(client);

                            //Customers c = (Customers) SelectClientIdField.getSelectedItem();
                            //System.out.println(c.getClientId());

                    }



                }else{
                    System.out.println("error");
                }

            }

            } catch (Exception e) {
                e.printStackTrace();
            }


        }

        });


        allclientsThread.start();

    }

