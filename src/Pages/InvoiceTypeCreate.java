    private void CreateInvoiceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateInvoiceBtnActionPerformed

        Customers c = (Customers) SelectClientIdField.getSelectedItem();

        GuestRadio.setActionCommand("1");
        RegisteredRadio.setActionCommand("2");
        String UserId = String.valueOf(cache.LogUserId);

        int ClientId = c.getClientId();

        String InvoiceType = select_client_type.getSelection().getActionCommand();
        //JOptionPane(this, "");

        if("1".equals(InvoiceType)){

            if(ClientContactNumber.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Please fill the requered(*)  area.");
            }else{

                String ClientName = clientName.getText();
                String ClientEmail = "unsaved email";
                String ClientTel = ClientContactNumber.getText();


                try {
                JSONObject jo = ServerController.getJsonObject(WebServiceController.CREATE_INVOICE_TYPE_DETAILS, ServerController.sendNewInvoiceParams(InvoiceType,ClientName,ClientEmail,ClientTel,ClientId,UserId));
                if(jo != null){

                    if(jo.getBoolean("result")){

                        int invoiceId = jo.getInt("in_id");

                        //stockGRNInvoiceCreate1.setGrnId(gid);
                        ClientContactNumber.setText("");
                        Home.showPanel(Home.invoiceNavView);

                        Invoice inv = new Invoice(invoiceId);
                        inv.setVisible(true);

                    }else{
                        System.out.println("error");
                        JOptionPane.showMessageDialog(this, "Error Creating GRN.");
                    }

                }

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }

        }else{
            if(ClientId == 0){
                JOptionPane.showMessageDialog(this, "Please fill the requered(*)  area.");
            }else{

                String ClientName = clientName.getText();
                String ClientEmail = "unsaved email";
                String ClientTel = ClientContactNumber.getText();


                try {
                JSONObject jo = ServerController.getJsonObject(WebServiceController.CREATE_INVOICE_TYPE_DETAILS, ServerController.sendNewInvoiceParams(InvoiceType,ClientName,ClientEmail,ClientTel,ClientId,UserId));
                if(jo != null){

                    if(jo.getBoolean("result")){

                        int invoiceId = jo.getInt("in_id");

                        ClientContactNumber.setText("");
                        Home.showPanel(Home.invoiceNavView);

                        Invoice inv = new Invoice(invoiceId);
                        inv.setVisible(true);

                    }else{
                        System.out.println("error");
                        JOptionPane.showMessageDialog(this, "Error Creating GRN.");
                    }

                }

                } catch (Exception e) {
                    e.printStackTrace();
                }


            }

        }

    }//GEN-LAST:event_CreateInvoiceBtnActionPerformed

