    private void LoadAllSupplierData(){





        dtm.setRowCount(0);


        allsuppliersThread = new Thread(new Runnable() {
        @Override
        public void run() {


            try {
                System.out.println(WebServiceController.DOWNLOAD_ALL_SUPPLIERS_LIST);
                 JSONObject jo = ServerController.getJsonObject(WebServiceController.DOWNLOAD_ALL_SUPPLIERS_LIST, ServerController.getSupplierListParams("1"));
            if(jo != null){

                if(jo.getBoolean("result")){

                    JSONArray array = jo.getJSONArray("data");

                    for(int i = 0; i<array.length() ;i++){

                            JSONObject pd = array.getJSONObject(i);

                            int SupplierId = pd.getInt("SupplierId");
                            int CountView = pd.getInt("CountView");

                            String SupplierName = pd.getString("SupplierName");
                            String SupplierAddress = pd.getString("SupplierAddress");
                            String SupplierNumber = pd.getString("SupplierNumber");



                            Vector v = new Vector();
                            v.add(CountView);
                            v.add(SupplierName);
                            v.add(SupplierNumber);
                            v.add(SupplierAddress);
                            v.add(SupplierId);

                            dtm.addRow(v);

                    }



                }else{
                    System.out.println("error");
                }





            }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        });


        allsuppliersThread.start();

    }

